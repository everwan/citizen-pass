export function getRoadSignAssetSource(stateCode: StateCode, signId: string) {
  if (stateCode === 'NY') {
    const nySource = nyOfficialRoadSignAssetOverrides[signId as keyof typeof nyOfficialRoadSignAssetOverrides];
    if (nySource) {
      return nySource;
    }
  }

  return roadSignAssetMap[signId as keyof typeof roadSignAssetMap];
}
